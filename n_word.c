#include "libft.h"

size_t		n_word(const char *str, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (*str)
	{
		if (j == 0 && *str != c)
		{
			j = 1;
			i++;
		}
		else if (j == 1 && *str == c)
			j = 0;
		str++;
	}
	return (i);
}
